using System;

class Program
{
    static void Main(string[] args)
    {
        // Создаем матрицу и заполняем ее данными
        int[,] matrix = new int[,]
        {
            { 1, 2, 3 },
            { 4, 5, 6 },
            { 7, 8, 9 }
        };

        // Инициализируем переменную для хранения максимального элемента и
        // присваиваем ей значение первого элемента матрицы
        int max = matrix[0, 0];

        // Перебираем все элементы матрицы, сравнивая их с текущим максимальным
        // элементом и обновляя значение переменной max, если находим элемент,
        // который больше текущего максимального элемента
        for (int i = 0; i < matrix.GetLength(0); i++)
        {
            for (int j = 0; j < matrix.GetLength(1); j++)
            {
                if (matrix[i, j] > max)
                {
                    max = matrix[i, j];
                }
            }
        }

        // Выводим максимальный элемент на экран
        Console.WriteLine("Максимальный элемент в матрице: {0}", max);

        // Ждем нажатия клавиши перед выходом из программы
        Console.ReadKey();
    }
}